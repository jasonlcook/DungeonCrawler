class Adventurer {
    constructor(health, strength, armour) {
        this.Health = health ;
        this.Strength = strength;
        this.Armour = armour;
    }

    getHealth() {
        return this.Health;
    }

    getStrength() {
        return this.Strength;
    }

    getArmour() {
        return this.Armour;
    }
}; 